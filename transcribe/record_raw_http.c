void intToStr(int num, char *str) {
    int i = 0, isNegative = 0;

    // Handle negative numbers
    if (num < 0) {
        isNegative = 1;
        num = -num;
    }

    // Extract digits and store them in reverse order
    do {
        str[i++] = (num % 10) + '0';
        num /= 10;
    } while (num > 0);

    // Add negative sign if necessary
    if (isNegative) {
        str[i++] = '-';
    }

    // Null-terminate the string
    str[i] = '\0';

    // Reverse the string
    for (int j = 0, k = i - 1; j < k; j++, k--) {
        char temp = str[j];
        str[j] = str[k];
        str[k] = temp;
    }
}

static void event_handler(void* arg, esp_event_base_t event_base, int32_t event_id, void* event_data)
{
    if (event_base == WIFI_EVENT && event_id == WIFI_EVENT_STA_START) {
        esp_wifi_connect();
    } else if (event_base == WIFI_EVENT && event_id == WIFI_EVENT_STA_DISCONNECTED) {
        if (s_retry_num < EXAMPLE_ESP_MAXIMUM_RETRY) {
            esp_wifi_connect();
            s_retry_num++;
            ESP_LOGI(TAG, "retry to connect to the AP");
        } else {
            xEventGroupSetBits(s_wifi_event_group, WIFI_FAIL_BIT);
        }
        ESP_LOGI(TAG,"connect to the AP fail");
    } else if (event_base == IP_EVENT && event_id == IP_EVENT_STA_GOT_IP) {
        ip_event_got_ip_t* event = (ip_event_got_ip_t*) event_data;
        ESP_LOGI(TAG, "got ip:" IPSTR, IP2STR(&event->ip_info.ip));
        //memcpy(&temp, (u32_t)(event->ip_info.ip), 4);
        ipaddr[0] = esp_ip4_addr1_16(&event->ip_info.ip);
        ipaddr[1] = esp_ip4_addr2_16(&event->ip_info.ip);
        ipaddr[2] = esp_ip4_addr3_16(&event->ip_info.ip);
        ipaddr[3] = esp_ip4_addr4_16(&event->ip_info.ip);
        for(int i = 0; i < 4; i++){
            intToStr(ipaddr[i], ipaddrstr[i]);
        }
        size_t offset = 0;
        for (int i = 0; i < 4; i++) {
            size_t len = strlen(ipaddrstr[i]);  // Get the length of the current string
            memcpy(ipAddrComplete + offset, ipaddrstr[i], len);  // Copy the current string
            offset += len;  // Update offset after copying the string
            if (i < 3) {  // Add the dot only between the IP parts, not at the end
                memcpy(ipAddrComplete + offset, &dot, 1);  // Copy the dot
                offset++;  // Increment offset for the dot
            }
        }
        s_retry_num = 0;
        xEventGroupSetBits(s_wifi_event_group, WIFI_CONNECTED_BIT);
    }
}

